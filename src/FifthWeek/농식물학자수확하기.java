package FifthWeek;

public class 농식물학자수확하기 extends 농식물학자가농사할때변화 implements Runnable {
	public void run() {
		if(생존자.체력<20) {
			System.out.println("몸에 힘이 없습니다. 현재체력이 20미만이기 때문에 행동을 취할 수 없습니다.");
		} else {
			if(생존자.체내수분<=19) {
				System.out.println("목이마릅니다. 체내수분이 20미만이기 때문에 행동을 취할 수 없습니다.");
				System.out.println("행동하기 위해선 물을 마셔주세요!");
			} else {
				if(생존자.포만감<=19) {
					System.out.println("배가고픕니다. 포만감이 20미만이기 때문에 행동을 취할 수 없습니다.");
					System.out.println("행동하기 위해선 감자를 먹어주세요!");
				} else {
					if(감자자랄때변화.다자란감자수==0) {
				     System.out.println("다자란 감자가 없습니다.");
				} else {
					for(int i=1;i<3;i++) {
						try {
							Thread.sleep(500);
						} catch(InterruptedException e) {
							e.printStackTrace();
						}
						if(생존자.체력<=0) {
							break;
						}
						생존자.체력 -= 2; 생존자.집중도 -= 2; 생존자.포만감 -=1; 생존자.체내수분 -=1;
						if(생존자.체력<0) {
							생존자.체력=0;
						}
						if(생존자.집중도<0) {
							생존자.집중도=0;
						}
						if(생존자.포만감<0) {
							생존자.포만감=0;
						}
						if(생존자.체내수분<0) {
							생존자.체내수분=0;
						}
						System.out.println("감자를 수확하는 작업을 시작합니다.고된 농삿일로 피곤해집니다.(체력 -2, 집중도-2, 포만감-1, 체내수분-1)" + " 현재체력 : " + 생존자.체력 + " 현재집중도 : " + 생존자.집중도 + " 현재포만감 : " +생존자.포만감 + " 현재체내수분 : " + 생존자.체내수분);
					}

						if(생존자.체력>0) {
							if(Math.random()<=농식물학자.농사성공확률) {
								try {
									Thread.sleep(1000);
								} catch(InterruptedException e) {
									e.printStackTrace();
								} 
								
								농식물학자.농사능력각성도++; 
								if(농식물학자.농사능력각성도>=60) {
									농식물학자.농사능력각성발휘 = true;
								}

								if(농식물학자.농사능력각성발휘 ==true) {
									농식물학자.농사능력 +=0.06;
									농식물학자.농식물학자농사고유확률 = 0.3;
									농식물학자.칭호 = "숙련된 농식물학자";
									농식물학자.농사능력각성 ="각성 완료";
								}
								
								감자자랄때변화.물거름주기로인한자라는정도증가 -= 0.1;
								if(감자자랄때변화.물거름주기로인한자라는정도증가<0) {
									감자자랄때변화.물거름주기로인한자라는정도증가=0;
								}
								
								생존자.남은감자수 += 감자자랄때변화.다자란감자수;
								감자자랄때변화.심은감자수=0; 
								배경음악 성공= new 배경음악("성공.mp3",false);
								성공.start();
								System.out.println("감자를 "+감자자랄때변화.다자란감자수+"개 수확했습니다."+" 현재남은감자수 : " + 생존자.남은감자수 +" 땅이 다시 척박해졌습니다. 감자성장확률 10%감소" + " 현재감자의성장확률: 3초당" + 감자자랄때변화.물거름주기로인한자라는정도증가*100 + "%");
								감자자랄때변화.다자란감자수=0; 감자자랄때변화.심은감자수=0;

							}else {
								
								배경음악 실패= new 배경음악("실패.mp3",false);
								실패.start();
								System.out.println("감자가 서로 엉켜있어 감자를 수확하는데 실패했습니다. 시간이지나 다시 시도해보세요.");		
							}






						}
					
				}
				}
			}
		}




	}
}
